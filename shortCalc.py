while True:
        num1 = int(input("type in first number: "))
        num2 = int(input("type in second number: "))
        opp = input("type in +, -, *, or /: ")
        if opp == "+":
                print(num1 + num2)
        elif opp == "-":
                print(num1 - num2)
        elif opp == "*":
                print(num1 * num2)
        elif opp == "/":
                print(num1 / num2)